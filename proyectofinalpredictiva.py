# 1. Cargar los datos desde el archivo .arff: 'ProyectoFinalMineriaEIA2024.arff'
datos_arff, meta = arff.loadarff('/content/ProyectoFinalMineriaEIA2024.arff')
data = pd.DataFrame(datos_arff)

