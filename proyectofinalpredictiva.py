"""### 2. Preparación de Datos: Variables Dummies y Codificación de Etiquetas (LabelEncoder)
Para que Scikit-Learn pueda trabajar eficientemente, es imprescindible que todas nuestras variables sean numéricas. En esta sección, aplicaremos `LabelEncoder` exclusivamente a nuestra variable objetivo. Para las variables predictoras categóricas, generaremos 'variables dummies'. Es de suma importancia seleccionar con precisión tanto la variable objetivo como las predictoras categóricas que se transformarán en dummies, ya que esto influirá directamente en el rendimiento de nuestros modelos.
"""

from sklearn.preprocessing import LabelEncoder
import numpy as np

# Reiniciar 'data' a partir de 'data_original' para asegurar un estado limpio
data = data_original.copy()

# --- IMPORTANTE: Aquí configuramos la variable objetivo y las variables categóricas predictoras ---
nombre_objetivo = "EstadoSituacion" # <<< 'EstadoSituacion' es la variable a predecir

# Convertir columnas que deberían ser numéricas a tipo numérico
# Identificación de columnas que son 'object' pero que deberían ser numéricas y podrían contener '?'
potential_numeric_cols = [
    'CapacidaddeVeranoMW', 'CapacidaddeInviernoMW', 'CargaMinimaMW',
    'MesdeOperacion', 'AniodeOperacion',
    'AumentoCapacidadVeranoPlanMW', 'AumentoCapacidadInviernoPlanMW',
    'MesAumentoPlanificado', 'AnioAumentoPlanificado',
    'ReduccionCapacidadVeranoPlanMW', 'ReduccionCapacidadInviernoPlanMW',
    'MesReduccionPlanificada', 'AnioReduccionPlanificada',
    'MesRepotenciacionPlan', 'AnioRepotenciacionPlan',
    'MesOtrasModificaciones', 'AnioOtrasModificaciones'
]

print("Convirtiendo columnas a tipo numérico y manejando '?'...")
for col in potential_numeric_cols:
    if col in data.columns and data[col].dtype == 'object':
        # Reemplazar '?' con NaN y convertir a numérico. Coerce errors para manejar cualquier otro valor no numérico
        data[col] = pd.to_numeric(data[col].replace('?', np.nan), errors='coerce')
        print(f"Columna '{col}' convertida a numérico. Valores no numéricos (incluyendo '?') convertidos a NaN.")

# Rellenar NaNs con la mediana (o media) para columnas numéricas que no son objetivo
for col in potential_numeric_cols:
    if col in data.columns and data[col].isnull().any():
        if col != nombre_objetivo: # No rellenar NaN en la variable objetivo todavía
            data[col] = data[col].fillna(data[col].median()) # Usamos la mediana para ser robustos a outliers
            print(f"NaNs en columna '{col}' rellenados con la mediana.")

print("data.info() después de rellenar NaNs para columnas numéricas:")
data.info()
print("\n")

# Dummies para predictoras con MÁS de 2 categorías (No se borra ninguna)
# Dummies para predictoras con SÓLO 2 categorías (Se borra una)

# Primero, identificar todas las columnas categóricas restantes que no son la variable objetivo
current_object_cols = data.select_dtypes(include=['object']).columns.tolist()
print(f"DEBUG: current_object_cols antes de eliminar el objetivo: {current_object_cols}")

if nombre_objetivo in current_object_cols:
    current_object_cols.remove(nombre_objetivo) # Excluir la variable objetivo
print(f"DEBUG: current_object_cols después de eliminar el objetivo (estas deben ser predictoras): {current_object_cols}")

# Variables que son categóricas y no son la variable objetivo
categorical_predictoras = current_object_cols

if categorical_predictoras:
    print(f"\nSe han identificado las siguientes columnas categóricas para crear dummies: {categorical_predictoras}")
    # Separar en 2 y >2 categorías
    two_category_cols = []
    multi_category_cols = []
    for col in categorical_predictoras:
        if data[col].nunique() == 2:
            two_category_cols.append(col)
        else:
            multi_category_cols.append(col)

    if two_category_cols:
        print(f"Creando dummies para columnas con 2 categorías (drop_first=True): {two_category_cols}")
        data = pd.get_dummies(data, columns=two_category_cols, drop_first=True, dtype=int)

    if multi_category_cols:
        print(f"Creando dummies para columnas con más de 2 categorías (drop_first=False): {multi_category_cols}")
        data = pd.get_dummies(data, columns=multi_category_cols, drop_first=False, dtype=int)
else:
    print("No se encontraron columnas categóricas predictoras para crear dummies.")

print("data.info() después de la creación de dummies (si aplica):")
data.info()
print("\n")

# 3. LabelEncoder EXCLUSIVO para la variable objetivo
if nombre_objetivo not in data.columns:
    raise ValueError(f"La variable objetivo '{nombre_objetivo}' no se encuentra en el DataFrame después de las transformaciones.")

# Manejar '?' o NaN en la variable objetivo antes de LabelEncoder
if data[nombre_objetivo].dtype == 'object' and '?' in data[nombre_objetivo].unique():
    print(f"Eliminando filas con '?' en la variable objetivo '{nombre_objetivo}'.")
    data = data[data[nombre_objetivo] != '?'].copy()
elif data[nombre_objetivo].isnull().any():
    print(f"Eliminando filas con NaN en la variable objetivo '{nombre_objetivo}'.")
    data = data.dropna(subset=[nombre_objetivo]).copy()

print(f"\nAplicando LabelEncoder a la variable objetivo: '{nombre_objetivo}'")
labelencoder = LabelEncoder()
data[nombre_objetivo] = labelencoder.fit_transform(data[nombre_objetivo])

print("\nPrimeras filas de los datos después de dummies y LabelEncoder:")
display(data.head())
print("Clases originales y codificadas de la variable objetivo:")
print(list(zip(labelencoder.classes_, labelencoder.transform(labelencoder.classes_))))
print("\nInformación general de los datos después de preparación:")
data.info()
print("\n")

"""### 3. División, Balanceo y Normalización de los Datos
Aquí, dividiremos nuestro conjunto de datos en dos partes: el 70% para entrenamiento y el 30% restante para prueba, asegurando una división estratificada para mantener la proporción de las clases. Luego, aplicaremos el método SMOTE *solo* al conjunto de entrenamiento para balancear las clases, lo cual es crucial para modelos robustos. Finalmente, normalizaremos las características numéricas, un paso esencial para algoritmos como KNN y las Redes Neuronales, pero mantendremos una copia de los datos sin normalizar para nuestro Árbol de Decisión.
"""

from sklearn.model_selection import train_test_split
from imblearn.over_sampling import SMOTE
from sklearn.preprocessing import MinMaxScaler

# Asegurar que nombre_objetivo sea el correcto para esta celda
nombre_objetivo = "EstadoSituacion"

# 1. Separar predictoras (X) y objetivo (Y)
X = data.drop(nombre_objetivo, axis=1)
Y = data[nombre_objetivo]

# --- IMPORTANTE:aquí se configuran las variables numéricas para la normalización ---
# Las variables tipo 'int64' o 'float64' que no son dummies suelen ser numéricas.
variables_numericas = X.select_dtypes(include=np.number).columns.tolist() # Identificación automática de numéricas
print(f"Variables numéricas identificadas para normalización: {variables_numericas}")

# 2. División 70/30 estratificada
print("\nDividiendo los datos en conjuntos de entrenamiento (70%) y prueba (30%) de forma estratificada...")
X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size=0.30, stratify=Y, random_state=42)
print(f"Dimensiones de X_train: {X_train.shape}, Y_train: {Y_train.shape}")
print(f"Dimensiones de X_test: {X_test.shape}, Y_test: {Y_test.shape}")

# 3. Balanceo de datos (Solo al Train)
print("\nBalanceando el conjunto de entrenamiento (SMOTE)...")
smote = SMOTE(random_state=42)
X_train_bal, Y_train_bal = smote.fit_resample(X_train, Y_train)
print(f"Dimensiones de X_train después de balanceo: {X_train_bal.shape}, Y_train después de balanceo: {Y_train_bal.shape}")
print("Distribución de la variable objetivo en Y_train_bal:")
print(pd.Series(Y_train_bal).value_counts())

# 4. Normalización (Solo para KNN, Redes y SVM)
print("\nNormalizando variables numéricas (MinMaxScaler)...")
min_max_scaler = MinMaxScaler()

# Copias para mantener las no normalizadas intactas para el Árbol
X_train_norm = X_train_bal.copy()
X_test_norm = X_test.copy()

if variables_numericas:
    # Fit solo en Train, Transform en Train y Test
    min_max_scaler.fit(X_train_bal[variables_numericas])
    X_train_norm[variables_numericas] = min_max_scaler.transform(X_train_bal[variables_numericas])
    X_test_norm[variables_numericas] = min_max_scaler.transform(X_test[variables_numericas])
    print("Variables numéricas normalizadas.")
else:
    print("No se encontraron variables numéricas para normalizar o la lista 'variables_numericas' está vacía.")

print("\nPrimeras filas de X_train_norm (normalizado):")
display(X_train_norm.head())

"""### 4. Creación y Evaluación de Modelos Individuales
Entrenamiento evaluación individual de los tres modelos de clasificación: el Árbol de Decisión, la Red Neuronal (MLP) y el algoritmo K-Nearest Neighbors (KNN).

#### A. Árbol de Decisión (Entrenamiento sin normalización)
El Árbol de Decisión, por su naturaleza, no exige que los datos estén normalizados. Por ello, lo entrenaremos directamente con los datos balanceados de nuestro conjunto de entrenamiento, sin aplicar ninguna normalización previa.
"""

from sklearn.tree import DecisionTreeClassifier, plot_tree
from sklearn import metrics
import matplotlib.pyplot as plt
import seaborn as sns
import numpy as np # Importar numpy

print("\n--- ENTRENANDO Y EVALUANDO ÁRBOL DE DECISIÓN ---")

# Aprendizaje
# Se usan datos balanceados (X_train_bal), NO normalizados (X_train_bal proviene de X_train_bal.copy() antes de normalizar)
# Se reduce max_depth para acelerar el entrenamiento del ensamble.
model_dt = DecisionTreeClassifier(criterion='gini', min_samples_leaf=2, max_depth=7, random_state=42) # Reducido de 10 a 7
model_dt.fit(X_train_bal, Y_train_bal)

# Predicción y Reporte
Y_pred_dt = model_dt.predict(X_test) # X_test no está normalizado para el árbol
print("\nReporte de Clasificación del Árbol de Decisión:")
print(metrics.classification_report(Y_test, Y_pred_dt, target_names=labelencoder.classes_))

# Visual: Gráfica del Árbol (puede ser muy grande para datasets complejos)
plt.figure(figsize=(25,15))
plot_tree(model_dt, feature_names=X.columns.tolist(), class_names=labelencoder.classes_, rounded=True, filled=True, fontsize=8)
plt.title("Árbol de Decisión")
plt.show()

# Visual: Matriz de Confusión
print("\nMatriz de Confusión del Árbol de Decisión:")
disp = metrics.ConfusionMatrixDisplay(confusion_matrix=metrics.confusion_matrix(Y_test, Y_pred_dt), display_labels=labelencoder.classes_)
disp.plot(cmap=plt.cm.Blues)
plt.title("Matriz de Confusión - Árbol de Decisión")
plt.show()

# Visual: Curva ROC para multiclase (One-vs-Rest)
print("\nCurva ROC del Árbol de Decisión (One-vs-Rest) para cada clase:")

y_score_dt = model_dt.predict_proba(X_test)

plt.figure(figsize=(10, 8))
for i, class_name in enumerate(labelencoder.classes_):
    # Binarizar Y_test para la clase actual (One-vs-Rest)
    y_test_bin = (Y_test == i).astype(int)

    # Calcular FPR, TPR y umbrales
    fpr, tpr, _ = metrics.roc_curve(y_test_bin, y_score_dt[:, i])

    # Graficar la curva ROC
    plt.plot(fpr, tpr, label=f'ROC de {class_name} (AUC = {metrics.auc(fpr, tpr):.2f})')

plt.plot([0, 1], [0, 1], 'k--', label='Clasificador aleatorio') # Línea de base
plt.xlabel('Tasa de Falsos Positivos (FPR)')
plt.ylabel('Tasa de Verdaderos Positivos (TPR)')
plt.title('Curva ROC - Árbol de Decisión (Multiclase One-vs-Rest)')
plt.legend(loc='lower right')
plt.grid(True)
plt.show()

