"""### 6. Ensambles: Votación por Mayoría (Hard) y Votación Ponderada (Soft)
Los métodos de ensamble son una estrategia poderosa para mejorar la capacidad predictiva de nuestros modelos, combinando las decisiones de varios clasificadores base. Exploraremos dos tipos: la Votación por Mayoría (Hard Voting), donde se elige la clase más votada, y la Votación Ponderada (Soft Voting), que asigna pesos a las probabilidades predichas por cada modelo.Este ensamble es el que menos procesamiento utiliza para la base de datos.
"""

from sklearn.ensemble import VotingClassifier

print("\n--- ENSAMBLES: VOTACIÓN HARD Y SOFT ---")

# Agrupamos nuestros clasificadores débiles. Usamos los modelos previamente entrenados.
# NOTA: Para VotingClassifier, los modelos deben tener el método `predict_proba` para 'soft' voting.
# DecisionTreeClassifier y MLPClassifier lo tienen. KNeighborsClassifier también.
