disp = metrics.ConfusionMatrixDisplay(confusion_matrix=metrics.confusion_matrix(Y_test, Y_pred_vot_hard), display_labels=labelencoder.classes_)
disp.plot(cmap=plt.cm.Oranges)
plt.title("Matriz de Confusión - Votación Hard")
plt.show()

# Visual: Curva ROC (solo para soft voting o si los clasificadores individuales tienen predict_proba)
# Para 'hard' voting, la curva ROC se calcula de manera diferente o no es directamente aplicable.
# Si quieres la curva ROC, se puede hacer con los modelos individuales o con soft voting.

